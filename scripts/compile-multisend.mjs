import solc from 'solc';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const srcPath = path.join(__dirname, '../contracts/MultiSend.sol');
const source = fs.readFileSync(srcPath, 'utf8');

const input = {
  language: 'Solidity',
  sources: { 'MultiSend.sol': { content: source } },
  settings: {
    optimizer: { enabled: true, runs: 200 },
    outputSelection: { '*': { '*': ['abi', 'evm.bytecode.object'] } },
  },
};

const out = JSON.parse(solc.compile(JSON.stringify(input)));
if (out.errors?.some((e) => e.severity === 'error')) {
  console.error(out.errors);
  process.exit(1);
}
const c = out.contracts['MultiSend.sol'].MultiSend;
const artifact = {
  abi: c.abi,
  bytecode: '0x' + c.evm.bytecode.object,
};
const dest = path.join(__dirname, '../src/lib/multisendArtifact.js');
fs.writeFileSync(
  dest,
  `/** Auto-generated by scripts/compile-multisend.mjs — do not edit */\n` +
    `export const MULTISEND_ABI = ${JSON.stringify(artifact.abi, null, 2)};\n` +
    `export const MULTISEND_BYTECODE = ${JSON.stringify(artifact.bytecode)};\n`
);
console.log('Wrote', dest, 'bytecode bytes', artifact.bytecode.length / 2 - 1);
